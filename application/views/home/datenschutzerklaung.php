Analysetools:<br>
Google Analytics
Rankalytics benutzt Google Analytics, einen Webanalysedienst der Google Inc. („Google“). Google Analytics verwendet sog. „Cookies“, Textdateien, die auf dem Computer gespeichert werden und die eine Analyse der Benutzung der Website durch den Nutzer ermöglichen. Die durch den Cookie erzeugten Informationen über die Benutzung von Spotwatch werden in der Regel an einen Server von Google in den USA übertragen und dort gespeichert. Aufgrund der Aktivierung der IP-Anonymisierung auf Spotwatch, wird die IP-Adresse des Nutzers von Google jedoch innerhalb von Mitgliedstaaten der Europäischen Union oder in anderen Vertragsstaaten des Abkommens über den Europäischen Wirtschaftsraum zuvor gekürzt. Nur in Ausnahmefällen wird die volle IP-Adresse an einen Server von Google in den USA übertragen und dort gekürzt. Im Auftrag von Spotwatch wird Google diese Informationen benutzen, um die Nutzung der Website auszuwerten, um Reports über die Websiteaktivitäten zusammenzustellen und um weitere mit der Websitenutzung und der Internetnutzung verbundene Dienstleistungen gegenüber dem Websitebetreiber zu erbringen. Die im Rahmen von Google Analytics vom Browser übermittelte IP-Adresse wird nicht mit anderen Daten von Google zusammengeführt. Die Speicherung der Cookies kann durch eine entsprechende Einstellung der Browser-Software verhindern werden; wir weisen jedoch darauf hin, dass in diesem Fall gegebenenfalls nicht sämtliche Funktionen von Spotwatch vollumfänglich genutzt werden können. Darüber hinaus kann man die Erfassung der durch das Cookie erzeugten und auf der Nutzung der Website bezogenen Daten (inkl. der IP-Adresse) an Google sowie die Verarbeitung dieser Daten durch Google verhindern, indem das unter dem folgenden Link verfügbare Browser-Plugin herunterladen und installieren wird: http://tools.google.com/dlpage/gaoptout?hl=de .
