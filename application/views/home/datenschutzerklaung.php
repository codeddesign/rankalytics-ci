		Personenbezogene Daten - Grundsätze<br>
Rankalytics erhebt, verarbeitet und nutzt personenbezogenen Daten von Nutzern ausschließlich unter Beachtung der geltenden datenschutzrechtlichen Bestimmungen.
Personenbezogene Daten sind alle Einzelangaben über persönliche oder sachliche Verhältnisse einer bestimmten oder bestimmbaren natürlichen Person. Hierunter fallen insbesondere sogenannte Bestandsdaten wie: Name, Anschrift, E-Mail-Adresse und Telefonnummer aber auch Nutzungsdaten können personenbezogene Daten sein, sofern sie Rückschlüsse auf eine Person zulassen – z.B. Information über den Zeitpunkt Ihres Besuchs unserer Website, den dabei von Ihnen verwendeten Browser und weitere beim Besuch unserer Seiten generierte Daten. 
<p></p>
Umfang der Datenerhebung, -verarbeitung und -nutzung<br>
Wir erheben, verarbeiten und nutzen personenbezogenen Daten unserer Nutzer nur für die Zwecke der Bereitstellung dieser Website. Darüber hinaus erheben, verarbeiten und nutzen wir Ihre personenbezogenen Daten nur im Rahmen etwaiger von Ihnen gesondert erteilter Einwilligungen.
Die Mitteilung personenbezogener Daten für den bloßen Besuch dieser Website ist nicht erforderlich. 
Beim Zugriff auf unsere Website werden folgende Informationen protokolliert:
Datum und Uhrzeit des Zugriffs, die URL (Internet-Adresse) der verweisenden Website, die abgerufene Datei, die übertragene Datenmenge, Meldung über den erfolgreichen Abruf, den Browsertyp und die Browserversion sowie Informationen über Ihr Betriebssystem. 
Wir verwenden diese Informationen ausschließlich zu statistischen Zwecken sowie zu internen Analysezwecken und zur Verbesserung unseres Angebots. Rückschlüsse auf eine Person sind uns dabei nicht möglich.
